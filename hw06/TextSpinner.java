public class TextSpinner {
    
    public static void main (String[] args){
        
        while(true){ //infinite loop, always true
            
            System.out.print("/"); //1st 
            System.out.print("\b");
            System.out.print("-"); //2nd 
            System.out.print("\b");
            System.out.print("\\"); //3rd 
            System.out.print("\b");
            System.out.print("|");
            System.out.print("\b"); //4th 
            
        }
        
    }
    
    
}